package manager;

public class AmobaSettingsException extends IllegalArgumentException {

	private static final long serialVersionUID = 1L;

	public AmobaSettingsException() {
		super();
	}

	public AmobaSettingsException(String message, Throwable cause) {
		super(message, cause);
	}

	public AmobaSettingsException(String s) {
		super(s);
	}

	public AmobaSettingsException(Throwable cause) {
		super(cause);
	}

}
